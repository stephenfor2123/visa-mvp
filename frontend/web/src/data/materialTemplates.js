export function getBankTemplate(countryCode) {
  if (!countryCode) return BANK_TEMPLATES.CN
  const schengen = ['FR', 'DE', 'IT', 'ES', 'NL', 'AT', 'CH', 'BE', 'GR', 'PT', 'CZ', 'HU', 'PL', 'SE', 'FI', 'DK', 'NO', 'IS']
  if (schengen.includes(countryCode)) return BANK_TEMPLATES.SG
  if (['US', 'UK', 'AU'].includes(countryCode)) return BANK_TEMPLATES.SG
  return BANK_TEMPLATES[countryCode] || BANK_TEMPLATES.CN
}

