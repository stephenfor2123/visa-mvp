export function getCountryNotes(countryCode, langKey = 'zh') {
  if (!countryCode) return []
  let key = countryCode
  const schengen = ['FR', 'DE', 'IT', 'ES', 'NL', 'AT', 'CH', 'BE', 'GR', 'PT', 'CZ', 'HU', 'PL', 'SE', 'FI', 'DK', 'NO', 'IS']
  if (schengen.includes(countryCode)) key = 'Schengen'
  const byLocale = COUNTRY_NOTES_BY_LOCALE[langKey] || COUNTRY_NOTES_BY_LOCALE.zh
  return byLocale[key] || []
}

